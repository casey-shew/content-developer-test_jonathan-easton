color = input('Guess my favorite color: ')

#if statement needs to check the variable color for some string value. Any color is fine.
if color == 'red':
    #Some output letting the user know they got the color correct.
    print('That is it!')
    print('Go Big Red!')
else: #else statement
    #Some output letting the user know they got the color wrong.
    print('This is not right.')

print ('Goodbye.')
